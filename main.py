def caesar_cipher(user_txt, direct, shift):
    shift %= 26

    if direct == "decode":
        shift *= -1

    cipher_text = ""

    for letter in user_txt:
        if letter in alphabet:
            position = alphabet.index(letter)
            new_position = position + shift
            new_letter = alphabet[new_position]
            cipher_text += new_letter
        else:
            cipher_text += letter

    print(f"The {direct}ed message is {cipher_text}.")


while True:
    direction = input("Type 'encode' to encrypt or 'decode' to decrypt: ").lower()
    user_input = input("Enter the message: ").lower()
    shift_amount = int(input("What's the shift amount?: "))

    caesar_cipher(user_txt=user_input, direct=direction, shift=shift_amount)

    if input("Do you want to continue?: ").lower() == "no":
        break
        
